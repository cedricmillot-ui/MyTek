<script>
const slides = document.querySelectorAll('.hero-slide');
const dots = document.querySelectorAll('.dot');
let current = 0;

// Affiche la slide et met à jour les dots
function showSlide(index) {
  slides.forEach((slide, i) => slide.classList.toggle('active', i === index));
  dots.forEach((dot, i) => {
    dot.classList.toggle('active', i === index);
    // Animation légère pour le dot actif
    if (i === index) {
      dot.style.transform = 'scale(1.4)';
      dot.style.transition = 'transform 0.3s';
      setTimeout(() => dot.style.transform = 'scale(1)', 300);
    }
  });
}

// Défilement suivant et précédent
function nextSlide() {
  current = (current + 1) % slides.length;
  showSlide(current);
}

function prevSlide() {
  current = (current - 1 + slides.length) % slides.length;
  showSlide(current);
}


const prevBtn = document.querySelector('.hero-prev');
const nextBtn = document.querySelector('.hero-next');

prevBtn.addEventListener('click', () => { prevSlide(); resetInterval(); });
nextBtn.addEventListener('click', () => { nextSlide(); resetInterval(); });


dots.forEach((dot, i) => {
  dot.addEventListener('click', () => {
    current = i;
    showSlide(current);
    resetInterval();
  });
});

// Intervalle automatique
let slideInterval = setInterval(nextSlide, 5000);
function resetInterval() {
  clearInterval(slideInterval);
  slideInterval = setInterval(nextSlide, 5000);
}

// Slide initial
showSlide(current);
</script>